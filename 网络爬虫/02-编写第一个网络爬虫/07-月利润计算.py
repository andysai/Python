# 定义月利润变量
i = input("请输入月利润:")

free_10 = float(i) * 0.1
free_20 = 100000 * 0.1 + (float(i) - 100000) * 0.075
free_30 = 100000 * 0.1 + (200000 - 100000) * 0.075 + (float(i) - 200000) * 0.05
free_40 = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + (float(i) - 400000) * 0.03
free_50 = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + (600000 - 400000) * 0.03 + (float(i) - 600000) * 0.015
free_60 = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + (600000 - 400000) * 0.03 + (1000000 - 600000) * 0.015 + (float(i) - 1000000) * 0.01

if i <= '100000':
    print(free_10)
elif '100000' < i <= '200000':
    print(free_20)
elif '200000' < i <= '400000':
    print(free_30)
elif '400000' < i <= '600000':
    print(free_40)
elif '600000' < i <= '1000000':
    print(free_50)
else:
    print(free_60)
